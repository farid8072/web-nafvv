    <p>
    Sampah plastik adalah limbah yang berasal dari bahan polimer sintetis yang 
    sulit terurai secara alami. Sebagian besar sampah plastik berasal dari kemasan makanan,
    botol minuman, kantong belanja, dan produk sekali pakai lainnya. 
    
    Di Indonesia, diperkirakan
    ada sekitar 64 juta ton sampah plastik setiap tahun, dengan 3,2 juta ton di antaranya berakhir di laut.
    Secara global, produksi sampah plastik mencapai 400 juta ton per tahun, dan sekitar 52 juta ton
    mencemari lingkungan (Our World in Data & Natural History Museum).
    </p>
    <p>Kebanyakan masyarakat mengolah sampah plastik dengan cara 
    dibakar ditempat terbuka, padahal membakar sampah sembarangan dapat
    menyebabkan berbagai dampak negatif, terutama pada kesehatan manusia dan 
    lingkungan. Pembakaran sampah juga berkontribusi pada pencemaran udara, 
    pembakaran di tempat terbuka menghasilkan berbagai emisi karbon, termasuk karbon 
    dioksida (CO2), metana (CH4), dan karbon hitam, yang berkontribusi pada pemanasan global
    dan mencemari udara.</p>

    <p>Dampak bagi lingkungan 
    Sampah plastik yang tidak terkelola dengan baik 
    mencemari ekosistem darat dan laut. Hewan-hewan sering kali tersangkut atau 
    menelan sampah plastik, menyebabkan cedera serius atau bahkan kematian.
    Serta Produksi plastik berkontribusi pada emisi gas rumah kaca,
    yang berdampak pada perubahan iklim dan pemanasan global
    </p>
    <p>
    Dampak bagi lautan
    Sampah plastik di laut memiliki efek luas, 
    mengancam kehidupan laut, serta menimbulkan kerugian ekonomi 
    dan sosial bagi masyarakat yang bergantung pada laut. Banyak hewan laut yang 
    terancam akibat menelan atau terjerat plastik, sehingga keberlangsungan hidup mereka terganggu.
    </p>
    <p>
        Polusi plastik tidak hanya mengancam kehidupan laut, tetapi juga hewan darat. 
        Hewan bisa menelan atau terlilit plastik, yang menyebabkan gangguan pencernaan, 
        luka serius, dan bahkan kematian. Mikroplastik yang menembus tanah dan sumber air
        juga merusak fauna tanah, mengurangi kesuburan, dan mengganggu keseimbangan ekosistem.
        Dampak ini tidak berhenti pada hewan saja, karena plastik juga dapat mencemari tanaman 
        dan makanan yang kita konsumsi, masuk ke rantai makanan manusia. Dengan demikian,
        polusi plastik menunjukkan efek yang luas dan serius bagi lingkungan, kesehatan hewan, dan manusia.
    </p>
    <p>
        Sampah plastik tidak hanya mencemari lingkungan dan lautan, 
        tetapi juga membahayakan kesehatan manusia. Zat kimia beracun yang terkandung di 
        dalamnya dapat masuk ke tubuh melalui udara, makanan, atau minuman yang terkontaminasi, 
        sehingga meningkatkan risiko berbagai jenis kanker, seperti kanker paru-paru,
         prostat, dan testis. Selain itu, paparan mikroplastik dan logam berat dari plastik bisa 
         merusak kulit, sistem saraf, pencernaan, pernapasan, serta mengganggu fungsi kelenjar endokrin 
         seperti tiroid. Racun yang dihasilkan juga berpotensi menimbulkan kerusakan hati dan ginjal.
    </p>
    <p>
    Pembakaran plastik melepaskan seluruh karbon yang tersimpan
    di dalamnya ke atmosfer dalam bentuk COâ‚‚, sekaligus menghasilkan 
    polutan berbahaya seperti dioksin, furan, dan merkuri. 
    Sekitar 40% limbah plastik global masih dibakar secara terbuka, 
    sehingga mempercepat pelepasan emisi karbon dan memperburuk 
    kualitas udara (The Conversation, 2023).
</p>
    <p>
        Emisi karbon dari pembakaran sampah, terutama plastik,
         tidak hanya mencemari udara dengan gas beracun yang berbahaya bagi 
         kesehatan manusia, tetapi juga merusak kualitas tanah dan air, 
         mengganggu ekosistem serta rantai makanan, mempercepat perubahan iklim, 
         dan pada akhirnya menurunkan kualitas hidup masyarakat secara menyeluruh.

    </p>
    <p>tips mengurangi emisi karbon
1. hemat energi, mengupayakan agar tidak menggunakan kendaran jika berpergian dekat
2. menggunakan transportasi tanpa bahan bakar, ketika berpergian menggunaka sepeda atau berjalan kaki
3. memilih bahal lokal, hal ini dapat mengurangi emisi dari barang impor
4. membawa botol minuman sendiri sehingga bisa isi ulang
5. menggunakan kantong belanja sendiri untuk mengurangi sampah plastik
6. membuang sampah pada tempatnya</p>

